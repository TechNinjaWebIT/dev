<ul class="list-icon circle-yes list-icon-plus">
<li>Random and scheduled laboratory drug testing</li>
<li>Mandatory attendance of 12 step recovery meetings</li>
<li>Nightly curfews</li>
<li>Individual house chores</li>
<li>Treating others with kindness and respect regardless of race, culture, sexual orientation, or ethnicity</li>
<li>Maintaining responsibility by attending school, volunteering, or being employed</li>
</ul>
