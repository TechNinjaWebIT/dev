<p>Each month we have <strong>community meetings</strong> where everyone gets together for food, fellowship, and news. We also have regular community functions and events such as holiday dinners and barbecues, birthday celebrations, and family outings to fun local attractions like the Miami Zoo.</p>
<h2>Sober Living at Mom and Pop&#8217;s House</h2>
<p>We provide structure for each member of Mom and Pop&#8217;s House with solid <strong>rules and guidelines</strong>. These guidelines help to maintain our standards of safe and sober living. Some of these are:</p>
